    cursor = conn.cursor()

    cursor.execute("DELETE FROM news")
    conn.commit()
    print("\n[清空] news 旧数据已删除")

    base_date = datetime.now().date()
    date_pool = [base_date - timedelta(days=i * random.randint(3, 8)) for i in range(len(NEWS_ITEMS))]
    date_pool.sort(reverse=True)

    inserted = 0
    for i, item in enumerate(NEWS_ITEMS):
        data = {}
        data['title'] = item['title']
        data['introduction'] = item['introduction']
        data['picture'] = random.choice(PICTURE_POOL)
        data['content'] = item['content']
        data['name'] = item['name']
        data['headportrait'] = random.choice(HEADPORTRAIT_POOL)
        data['addtime'] = datetime.now()

        cols = ', '.join(data.keys())
        qmarks = ', '.join(['%s'] * len(data))
        sql = f"INSERT INTO news ({cols}) VALUES ({qmarks})"

        try:
            cursor.execute(sql, tuple(data.values()))
            conn.commit()
            inserted += 1
            print(f"  [{inserted:02d}] {data['title'][:40]} | {data['name']}")
        except Exception as e:
            print(f"  [ERROR] {e}")
            conn.rollback()

    conn.close()

    print("\n" + "=" * 60)
    print(f"  完成！共插入 {inserted} 条系统公告")
